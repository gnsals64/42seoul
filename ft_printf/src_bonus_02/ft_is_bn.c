bool	ft_istype(char a)
{
	return (a == 'c' || a == 's' || a == 'p' || a == 'd' || a == 'i'
		|| a == 'u' || a == 'x' || a == 'X' || a == '%');
}

bool	ft_isflag(char a)
{
	return (a == '-' || a == '+' || a == ' ' || a == '0' || a == '#');
}

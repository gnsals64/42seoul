#include <cub3d.h>

void	init_tex(t_data *data)
{
	int	i;

	i = 0;
	while (i < 4)
	{
		data->tex.tex[i] = malloc(sizeof(int *) * TEX_H * TEX_W);
		i++;
	}
}
